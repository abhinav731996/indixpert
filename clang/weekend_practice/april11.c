// Sum * Multipluction of two numbers 

#include<stdio.h>
int main()
{
    int firstnum = 1000;
    int secnum = 500;
    int sum = firstnum + secnum;
    int prod = firstnum + secnum;
    printf("%d + %d = %d\n", firstnum, secnum, sum);
    printf("%d * %d = %d\n", firstnum, secnum, prod);

    return 0;
}